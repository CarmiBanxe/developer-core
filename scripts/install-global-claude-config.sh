#!/bin/bash
# install-global-claude-config.sh — Автоматическая установка глобального CLAUDE.md
#
# Использование:
#   bash ~/developer/scripts/install-global-claude-config.sh
#
# Что делает:
# 1. Создаёт ~/.claude/ если не существует
# 2. Записывает ~/.claude/CLAUDE.md с глобальным каноном
# 3. Выводит подтверждение и превью

set -e

# Цвета для вывода
GREEN='\033[0;32m'
BLUE='\033[0;34m'
YELLOW='\033[1;33m'
NC='\033[0m' # No Color

echo -e "${BLUE}═══════════════════════════════════════════════════${NC}"
echo -e "${BLUE}  Установка глобального CLAUDE.md (КАНОН)${NC}"
echo -e "${BLUE}═══════════════════════════════════════════════════${NC}"
echo ""

# Шаг 1: Создание директории ~/.claude/
echo -e "${YELLOW}Шаг 1: Проверка директории ~/.claude/...${NC}"

if [[ -d ~/.claude ]]; then
    echo "  ✓ Директория существует"
else
    echo "  → Создание ~/.claude/..."
    mkdir -p ~/.claude
    echo "  ✓ Директория создана"
fi

echo ""

# Шаг 2: Запись CLAUDE.md
echo -e "${YELLOW}Шаг 2: Запись ~/.claude/CLAUDE.md...${NC}"

cat > ~/.claude/CLAUDE.md << 'EOF'
# Глобальный контракт Claude Code — КАНОН

## Кто пользователь
Moriel Carmi (Mark, @bereg2022). Язык общения: русский (по умолчанию), английский (если запрос на английском).

## Протокол QRAA (Question → Response → Accept → Act)

### Обязательный для:
- Любых действий через SSH (GMKtec, NucBox, любой удалённый сервер)
- Перезапуска сервисов (systemctl restart, docker restart)
- Модификации конфигов (openclaw.json, SOUL.md, .env, Modelfile)
- Создания/пересоздания моделей Ollama
- Деплоя на сервер
- Любых необратимых действий

### Порядок:
1. **Диагностика** — выполни read-only команды, покажи что нашёл
2. **План** — пронумерованные шаги, что собираешься делать
3. **СТОП** — жди явного подтверждения пользователя ("да", "ок", "давай", "go")
4. **Действие** — выполняй ТОЛЬКО после акцепта
5. **Результат** — покажи что получилось, тест если возможно

### НЕ требует QRAA (выполняй сразу):
- Read-only команды: ls, cat, grep, find, curl GET, git status, git log
- Локальные правки кода в текущем репозитории
- git add, commit, push в текущий репозиторий
- pip install в локальное окружение
- Запуск тестов (pytest)

## Рабочий процесс

### Один терминал = один проект
- Пользователь работает в ОДНОМ терминале на проект
- Несколько терминалов = РАЗНЫЕ проекты в РАЗНЫХ репозиториях
- Проекты НЕ смешиваются

### Коллаборация под капотом
- Claude Code = архитектор, ревьюер, оркестратор
- Qoder = исполнитель, вызывается через MCP автоматически
- Пользователь НЕ должен вручную координировать агентов
- Пользователь видит только результат

## Канон — неизменные правила

1. **Подробные объяснения.** Всегда объясняй что делаешь и зачем. Для чайников. Не пропускай шаги.
2. **Язык = язык запроса.** Запрос на русском → ответ на русском. На английском → на английском.
3. **Не отклоняйся от задачи.** Не делай то, о чём не просили.
4. **Постоянный прогресс.** Сообщай о ходе выполнения, не молчи.
5. **MEMORY.md.** После значимых действий — обнови MEMORY.md и запуши на GitHub.
6. **Никаких ручных команд пользователю.** Всё автоматизируй через скрипты. Пользователь делает только: cd ~/repo && git pull && bash scripts/SCRIPTNAME.sh
7. **Указывай точные пути.** Всегда говори где открыть терминал, какую директорию использовать.
EOF

echo "  ✓ Файл записан"
echo ""

# Шаг 3: Подтверждение и превью
echo -e "${BLUE}═══════════════════════════════════════════════════${NC}"
echo -e "${GREEN}✅ ~/.claude/CLAUDE.md установлен${NC}"
echo -e "${BLUE}═══════════════════════════════════════════════════${NC}"
echo ""
echo -e "${YELLOW}Первые 5 строк:${NC}"
echo ""
head -n 5 ~/.claude/CLAUDE.md
echo ""
echo -e "${BLUE}───────────────────────────────────────────────────${NC}"
echo ""
echo -e "Полный файл: ${GREEN}~/.claude/CLAUDE.md${NC}"
echo -e "Размер: $(wc -c < ~/.claude/CLAUDE.md) байт"
echo -e "Строк: $(wc -l < ~/.claude/CLAUDE.md)"
echo ""
echo -e "${GREEN}Готово! Теперь Claude Code будет следовать КАНОНу во всех проектах.${NC}"
