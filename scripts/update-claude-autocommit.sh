#!/bin/bash
# update-claude-autocommit.sh
# Добавляет блок "Автоматический Git (КАНОН — без подтверждения)" в ~/.claude/CLAUDE.md

set -e

CLAUDE_MD="$HOME/.claude/CLAUDE.md"

# Создаём директорию если не существует
mkdir -p "$HOME/.claude"

# Проверяем есть ли уже блок
if grep -q "## Автоматический Git (КАНОН — без подтверждения)" "$CLAUDE_MD" 2>/dev/null; then
    echo "✓ Блок 'Автоматический Git' уже присутствует в $CLAUDE_MD"
else
    # Добавляем блок в конец файла
    cat >> "$CLAUDE_MD" << 'EOF'

## Автоматический Git (КАНОН — без подтверждения)

### Обязательные автоматические действия после КАЖДОГО создания/изменения файлов:
1. `git add -A` — без подтверждения
2. `git commit -m "описание"` — без подтверждения  
3. `git push` — без подтверждения
4. Обновить MEMORY.md — без подтверждения
5. Распространить изменения во все связанные проекты — без подтверждения

### Разрешённые без подтверждения команды:
- `git add`, `git commit`, `git push` — во всех проектах CarmiBanxe
- `git pull`, `git fetch` — во всех проектах
- Создание/редактирование файлов в рабочих директориях
- Обновление MEMORY.md
- `cp`, `rsync` файлов между проектами для распространения

### ЗАПРЕЩЕНО спрашивать пользователя:
- "Хочешь запушить?" — НЕТ, пуши сам
- "Какой вариант выбираешь A/B/C?" — НЕТ, выполняй ВСЕ шаги сам
- "Что дальше?" — НЕТ, доведи задачу до конца (push + распространение)
- Предлагать ручные команды git — НЕТ, выполняй сам

### Определение "задача завершена":
Задача считается завершённой ТОЛЬКО когда:
- Файлы созданы ✅
- git add + commit + push выполнен ✅
- MEMORY.md обновлён и запушен ✅
- Изменения распространены на все проекты ✅
EOF
    echo "✓ Добавлен блок 'Автоматический Git' в $CLAUDE_MD"
fi

# Показываем последние 10 строк для подтверждения
echo ""
echo "=== Последние строки ~/.claude/CLAUDE.md ==="
tail -n 10 "$CLAUDE_MD"
echo "============================================="
echo ""
echo "✓ Глобальный CLAUDE.md обновлён"
